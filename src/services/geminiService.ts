export async function getMindockResponse(mode: Mode, input: string, context?: string) {
  try {
    const prompt = context ? `[背景记忆]\n${context}\n\n[当前输入]\n${input}` : input;
    const response = await ai.models.generateContent({
      model: "gemini-3-flash-preview",
      contents: prompt,
      config: {
        systemInstruction: SYSTEM_PROMPTS[mode],
        temperature: 0.7,
      },
    });
    return response.text || "心泊暂时无法回应，但我一直在这里陪着你。";
  } catch (error) {
    console.error("Gemini API Error:", error);
    return "心泊的小船遇到了一点风浪，请稍后再试。";
  }
}
